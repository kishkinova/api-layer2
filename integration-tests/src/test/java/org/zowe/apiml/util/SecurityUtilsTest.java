package org.zowe.apiml.util;

import io.jsonwebtoken.Claims;
import org.junit.jupiter.api.Nested;
import org.junit.jupiter.params.ParameterizedTest;
import org.junit.jupiter.params.provider.CsvSource;

import static org.junit.jupiter.api.Assertions.assertEquals;

class SecurityUtilsTest {

    @Nested
    class JwtParser {

        @ParameterizedTest
        @CsvSource({
            "eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9.eyJzdWIiOiIxMjM0NTY3ODkwIiwibmFtZSI6IkpvaG4gRG9lIiwiaWF0IjoxNTE2MjM5MDIyfQ.SflKxwRJSMeKKF2QT4fwpMeJf36POk6yJV_adQssw5c,John Doe",
            "eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9.eyJzdWIiOiIxMjM0NTY3ODkwIiwibmFtZSI6IkpvaG4gRG9lIiwiaWF0IjoxNTE2MjM5MDIyfQ.,John Doe",
        })
        void givenNoKey_whenParseJwtStringUnsecure_thenParseIt(String jwt, String name) {
            Claims claims = SecurityUtils.parseJwtStringUnsecure(jwt);
            assertEquals(name, claims.get("name", String.class));
        }

    }

}